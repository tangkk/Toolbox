function notename = num2notename(num)

switch num
    case 0
        notename = 'C';
    case 1
        notename = 'C#';
    case 2
        notename = 'D';
    case 3
        notename = 'D#';
    case 4
        notename = 'E';
    case 5
        notename = 'F';
    case 6
        notename = 'F#';
    case 7
        notename = 'G';
    case 8
        notename = 'G#';
    case 9
        notename = 'A';
    case 10
        notename = 'A#';
    case 11
        notename = 'B';
end
